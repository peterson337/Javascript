                                            // Variável

// Variável é referência ao espaço na memória do computador para cadar informação, tem esse nome por que variá
// o valor da execução da aplicação, na programação para criar variável usa var, let e const.
// As variáveis var, let e const quando você cria uma variável, exemplo: nome, não pode mudar a primeira letra
// ou deixar tudo em maiúsculo, pois o javascript não vai entender.

                                            // Var
// O var pode é parecido com o let a diferencia entre eles é que o var pode escapar, então é considerado de mal
// uso. O var da para vc colocar a variável fora do consoole.log.

var voPaPraia = "Yuji Itadori"

console.log(
    voPaPraia
)

//                                          ou

console.log(
voPaPraia = "Subaru Natsuki"
)
         

                                           // let

//  O let é   igua a var a diferença é que não considerado mal uso. O Let não da para vc colocar a varável fora
// do console.log.
let voPaPraia = "Yuji Itadori"

console.log(
    voPaPraia
)

//                                           ou

console.log(
voPaPraia = "Subaru Natsuki"
)

                                         // const 

// o const a diferença para  as outras variáveis é que não da para substituir o que vc coloca na varável.

let voPaPraia = "Yuji Itadori"

console.log(
voPaPraia
)
         
                                           
                                           



